void TIM1_CC_IRQHandler(void)
{
	if((TIM1CH4_CAPTURE_STA&0X80) == 0)
	{
		if (TIM_GetITStatus(TIM1, TIM_IT_Update) != RESET)
		{	
			  //通道1					
				if(TIM1CH4_CAPTURE_STA&0X40)//已经捕获到高电平了
				{
					if((TIM1CH4_CAPTURE_STA&0X3F)==0X3F)//高电平太长了
					{
						TIM1CH4_CAPTURE_STA|=0X80;
						TIM1CH4_CAPTURE_VAL = 0xFFFF;					
					}
					else
					{
						TIM1CH4_CAPTURE_STA++;
					}
				}
		}

		//通道1
		if (TIM_GetITStatus(TIM1, TIM_IT_CC4) != RESET) 		//捕获1发生捕获事件
		{	
			if (TIM1CH4_CAPTURE_STA & 0X40)		//捕获到一个下降沿
			{
				TIM1CH4_CAPTURE_STA|=0X80;	
				TIM1CH4_CAPTURE_VAL = TIM_GetCapture4(TIM1);//记录下此时的定时器计数值
				TIM_OC4PolarityConfig(TIM1, TIM_ICPolarity_Rising); //设置为上升沿捕获					
			}
			else //发生捕获时间但不是下降沿，第一次捕获到上升沿，记录此时的定时器计数值
			{
				TIM1CH4_CAPTURE_STA = 0;
				TIM1CH4_CAPTURE_VAL = 0;
				TIM1CH4_CAPTURE_STA |= 0X40;		//标记已捕获到上升沿
				TIM_SetCounter(TIM1, 0);		//清空计数器
				TIM_OC4PolarityConfig(TIM1, TIM_ICPolarity_Falling);//设置为下降沿捕获
				
			}
		}
	}
    TIM_ClearITPendingBit(TIM1, TIM_IT_CC4|TIM_IT_Update); 		//清除中断标志位		
}



